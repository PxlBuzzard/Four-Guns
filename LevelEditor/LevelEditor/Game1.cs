        /// and initialize them as well.
        /// </summary>
        protected override void Initialize()
        {                       
            base.Initialize();

            tileMap.Layers.Add(tileLayer);
            tileMap.Layers.Add(tileLayer2);
            tileMap.Layers.Add(tileLayer3);
        }

        /// <summary>
        /// LoadContent will be called once per game and is the place to load
        /// all of your content.
        /// </summary>
        protected override void LoadContent()
        {
            // Create a new SpriteBatch, which can be used to draw textures.
            spriteBatch = new SpriteBatch(GraphicsDevice);

            // loads the textures to be used
            tileLayer = TileLayer.FromFile(Content, "Content/Layers/Layer1.layer");

            tileLayer2 = TileLayer.FromFile(Content, "Content/Layers/CollisionLayer.layer");

            tileLayer3.LoadTileTextures(Content,
                "Tiles/3");
            tileLayer3.Alpha = .5f;
        }

        /// <summary>
        /// UnloadContent will be called once per game and is the place to unload
        /// all content.
        /// </summary>
